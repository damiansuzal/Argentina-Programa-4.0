# Pedir lado y alto de un cuadrilátero y decir si es cuadrado o rectángulo.
base = int(input("Ingrese la base del cuadrilatero: "))
altura = int(input("Ingrese la altura del cuadrilatero: "))
if base == altura:
    print("El cuadrilatero es Cuadrado")
else:
    print("El cuadrilatero es Rectangular")
